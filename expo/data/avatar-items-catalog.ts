/**
 * Get items by business category
 */
export function getItemsByBusinessCategory(categoryId: string): AvatarItemCatalog[] {
  const categoryMap: Record<string, string[]> = {
    cat_real_estate: ['business', 'formal', 'real_estate'],
    cat_retail: ['casual', 'everyday'],
    cat_medical: ['medical', 'healthcare', 'professional'],
    cat_financial: ['business', 'formal', 'luxury', 'premium'],
    cat_restaurant: ['food', 'culinary'],
    cat_tech_startup: ['tech', 'casual', 'startup'],
    cat_professional: ['business', 'formal', 'professional'],
    cat_construction: ['work', 'construction', 'safety'],
    cat_creative: ['artistic', 'creative', 'photography'],
    cat_education: ['academic', 'education', 'formal'],
    cat_personal_services: ['wellness', 'spa', 'relaxation'],
    cat_transportation: ['service', 'transportation', 'professional'],
    cat_manufacturing: ['work', 'industrial', 'manufacturing'],
    cat_agriculture: ['outdoor', 'farming'],
    cat_entertainment: ['entertainment', 'music', 'vip'],
    cat_hospitality: ['service', 'hospitality', 'travel'],
    cat_wellness: ['fitness', 'athletic', 'yoga'],
    cat_automotive: ['automotive', 'work', 'mechanic'],
    cat_pet_services: ['pet', 'animal'],
    cat_ecommerce: ['tech', 'remote', 'online'],
  };

  const relevantTags = categoryMap[categoryId] || ['casual'];

  return AVATAR_ITEMS_CATALOG.filter((item) => {
    return (
      relevantTags.includes(item.category) ||
      item.tags?.some((tag) => relevantTags.includes(tag))
    );
  });
}

/**
 * Get items by rarity
 */
export function getItemsByRarity(rarity: 'common' | 'uncommon' | 'rare' | 'epic' | 'legendary'): AvatarItemCatalog[] {
  return AVATAR_ITEMS_CATALOG.filter((item) => item.rarity === rarity);
}

/**
 * Get items by item type
 */
export function getItemsByType(itemType: string): AvatarItemCatalog[] {
  return AVATAR_ITEMS_CATALOG.filter((item) => item.item_type === itemType);
}