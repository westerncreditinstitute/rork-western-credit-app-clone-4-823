export const calculateStartupFunding = (
  businessType: BusinessType,
  ownCapital: number
): { needsInvestors: boolean; fundingGap: number; suggestedInvestorAmount: number } => {
  const avgStartupCost = (businessType.startupCostMin + businessType.startupCostMax) / 2;
  const fundingGap = Math.max(0, avgStartupCost - ownCapital);
  const needsInvestors = fundingGap > 0;
  const suggestedInvestorAmount = Math.ceil(fundingGap / 1000) * 1000;
  
  return { needsInvestors, fundingGap, suggestedInvestorAmount };
};
