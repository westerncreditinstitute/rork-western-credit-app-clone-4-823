export const getCategoryIcon = (categoryType: string): string => {
  switch (categoryType) {
    case 'retail': return 'Store';
    case 'service': return 'Wrench';
    case 'manufacturing': return 'Factory';
    case 'professional': return 'Briefcase';
    case 'technology': return 'Laptop';
    case 'medical': return 'Stethoscope';
    case 'financial': return 'TrendingUp';
    case 'real_estate': return 'Building';
    default: return 'Building2';
  }
};

export const getRiskColor = (risk: RiskLevel): string => {
  switch (risk) {
    case 'low': return '#22C55E';
    case 'medium': return '#F59E0B';
    case 'high': return '#EF4444';
    case 'very_high': return '#DC2626';
    default: return '#6B7280';
  }
};

export const getRiskLabel = (risk: RiskLevel): string => {
  switch (risk) {
    case 'low': return 'Low Risk';
    case 'medium': return 'Medium Risk';
    case 'high': return 'High Risk';
    case 'very_high': return 'Very High Risk';
    default: return 'Unknown';
  }
};

export const formatCurrency = (amount: number): string => {
  return `$${amount.toLocaleString('en-US', { minimumFractionDigits: 0, maximumFractionDigits: 0 })}`;
};
